/// Spacing tokens for consistent layout
/// Following 8px grid system for scalability
class Spacing {
  Spacing._();

  // Base unit
  static const double baseUnit = 8.0;

  // Spacing scale
  static const double xs = baseUnit; // 8
  static const double sm = baseUnit * 2; // 16
  static const double md = baseUnit * 3; // 24
  static const double lg = baseUnit * 4; // 32
  static const double xl = baseUnit * 5; // 40
  static const double xxl = baseUnit * 6; // 48
  static const double xxxl = baseUnit * 8; // 64
  static const double huge = baseUnit * 10; // 80
  static const double massive = baseUnit * 12; // 96
}




